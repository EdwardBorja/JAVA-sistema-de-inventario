    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            JFileChooser chooser=new JFileChooser();
            chooser.setAcceptAllFileFilterUsed(false);
            FileFilter filter = new FileNameExtensionFilter("CSV file", "csv");
            chooser.addChoosableFileFilter(filter);
            chooser.showOpenDialog(null);

            File f=chooser.getSelectedFile();
            String filename= f.getAbsolutePath();

            //aqui cambia las digonales
            rut.setText(filename.replace("\\","/"));
                // System.out.println(filename.replace("\\","/"));

                }catch(java.lang.Exception e){}
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(rut.getText().equals("")){JOptionPane.showMessageDialog(null,"Selecciona un archivo");
        }else{

            String Ruta;
            Ruta=rut.getText();

            Statement  stmt = null;

            try {

                 stmt = cn.createStatement();

                 
                stmt.executeUpdate("load data local infile \'"+ Ruta +"\'"
                    + "into table conductor fields terminated by \',\'"
                    + "lines terminated by \'\\n\'" 
                    +"IGNORE 1 LINES "
                 
                );
                
                JOptionPane.showMessageDialog(null,"Se import√≥ la informacion exitosamente");

            }
            catch( SQLException e) {
                e.printStackTrace();
              JOptionPane.showMessageDialog(null,"No se importo la informacion");
            }

            dispose();

        }
    }//GEN-LAST:event_jButton3ActionPerformed

